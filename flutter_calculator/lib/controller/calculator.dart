class Calculator {
  double num1;
  double num2;
  // Operations methods

  Calculator(this.num1, this.num2) {
    num1 = num1;
    num2 = num2;
  }

  double sum() {
    return num1 + num2;
  }

  double sub() {
    return num1 - num2;
  }

  double div() {
    return num1 / num2;
  }

  double mul() {
    return num1 * num2;
  }
}
